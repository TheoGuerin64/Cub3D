char	*ft_strcat(char *dest, const char *src)
{
	int	i;

	i = 0;
	while (dest[i])
		i++;
	while (src[0])
	{
		dest[i] = src[0];
		i++;
		src++;
	}
	dest[i] = '\0';
	return (dest);
}
